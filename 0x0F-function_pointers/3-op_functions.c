#include "calc.h"

/**
 * op_add - addition function
 * @a: interger 1
 * @b: integer 2
 * Add two integer
 * Return: integer
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction function
 * @a: integer 1
 * @b: integer 2
 * Subtract b from a
 * Return: integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/** op_mul - multiplication function
 * @a: integer 1
 * @b: integer 2
 * Multiply two integers
 * Return: integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division function
 * @a: integer 1
 * @b: integer 2
 * Divide integers
 * Return: integers
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - module function
 * @a: integer 1
 * @b: integer 2
 * Module
 * Return: integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
